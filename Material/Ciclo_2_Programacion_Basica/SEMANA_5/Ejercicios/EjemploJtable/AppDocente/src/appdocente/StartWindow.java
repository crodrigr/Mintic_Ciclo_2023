    public boolean validarCampos(){
        if( !textNombre.getText().equals("") && 
            !textDocumento.getText().equals("") &&
            !textHoras.getText().equals("")){
           return true;
        }
        return false;                           
    }
    
    public void limpiar(){
       textNombre.setText("");
       textDocumento.setText("");
       //textCategoria.setText("");
       textHoras.setText("");
       labelError.setText("");
    
    }
    
    public void mostrar(){
    
      String[][] matris=new String[this.docentes.size()][5];
      
      for(int i=0;i<this.docentes.size();i++){
          matris[i][0]=this.docentes.get(i).getNombre();
          matris[i][1]=this.docentes.get(i).getDocumento();
          matris[i][2]=Integer.toString(this.docentes.get(i).getCategoria());
          matris[i][3]=Integer.toString(this.docentes.get(i).getHorasTrabajas());
          matris[i][4]=Double.toString(this.docentes.get(i).getSalario());
          jTableDocentes.setModel(new javax.swing.table.DefaultTableModel(
                                  matris,new String[]{
                                   "Nombre","Documento","Categoria","Horas","Honorarios"
                                  }));
      }
      
      
    
    }
    
